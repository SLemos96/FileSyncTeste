    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        int porta;
        
        if (verificarLogin()) {
            porta = Integer.parseInt(portaField.getText());
            fileSync.iniciarCliente();
            if (fileSync.autenticarServidor(IPDestino.getText(), 
                    porta) ) {
                new MainScreen().setVisible(true);
                //avisoDeLogin1.setText("Conex√£o estabelecida");
                //avisoDeLogin1.setVisible(true);
            } else {
                avisoDeLogin.setText("Sevidor na " + porta+ " n√£o encontrado");
            }
        }
        else {
            avisoDeLogin.setText("Usu√°rio ou Senha incorreta!");
        }
        
    }//GEN-LAST:event_LoginButtonActionPerformed

