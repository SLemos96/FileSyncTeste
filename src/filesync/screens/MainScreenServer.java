    public MainScreenServer() {
        initComponents();

        try {

            server_skt = new ServerSocket(12345);
            ArrayList directory = new ArrayList();

            while (true) {

                directory.clear();

                skt = server_skt.accept();
                input = new ObjectInputStream(skt.getInputStream());

                Request req = (Request) input.readObject();

                switch (req.getType()) {
                    case "leitura":
                        File choose = new File(req.getPath().getAbsolutePath());
                        directory = preenche(choose);
                        output = new ObjectOutputStream(skt.getOutputStream());
                        output.writeObject(directory);
                        break;
                    case "download":
                        break;
                    case "upload":
                        break;
                }
            }

        } catch (IOException e) {
            System.out.println(" erro ai");
        } catch (ClassNotFoundException e2) {
            System.out.println("falha ao encontrar pasta selecionada");
        }

    }

