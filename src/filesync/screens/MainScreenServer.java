    public ArrayList preenche(File choose) {

        ArrayList absolute = new ArrayList();
        
        for (File arquivo : choose.listFiles()) {
            if (arquivo.isFile()) {
                absolute.add(new Node_info(arquivo.getName(), arquivo.lastModified(), null));
            } else {
                absolute.add(preenche(arquivo));
            }
        }

        return absolute;

    }
    

